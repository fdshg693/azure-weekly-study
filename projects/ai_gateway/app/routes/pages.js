//
// ここで完成させる体験:「デプロイ一覧 / 新規デプロイ / 削除 / チャット試し打ち」を
// 同じ画面で行き来し、"作ったモデルをその場で呼んで結果が返る" を 1 画面で味わう。
// ============================================================================

const express = require("express");

const router = express.Router();

// 管理 UI（トップ画面）。env の値はクライアント JS では使わないので最小限だけ渡す。
router.get("/", (_req, res) => {
  res.render("index", {
    title: "AI Gateway 管理 UI",
    // 既定デプロイ名（チャット試し打ちの初期選択に使う）。未設定なら空文字。
    defaultDeployment: process.env.AZURE_OPENAI_DEPLOYMENT || "",
  });
});

module.exports = router;
